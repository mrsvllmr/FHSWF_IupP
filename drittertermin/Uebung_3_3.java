package drittertermin;

class Uebung_3_3 {
   public static void main(String[] args){
      int x = 10; 
      int y = -1; 
      System.out.println("x >> 1 = " + (x >> 1)); 
      System.out.println("x >>> 1 = " + (x >>> 1)); 
      System.out.println("y >> 2 = " + (y >> 2)); 
      System.out.println("y >> 15 = " + (y >> 15));      
      System.out.println("y >>> 2 = " + (y >>> 2)); 
   }
}
