package drittertermin;

class BitoperatorenBeispiel { 
   public static void main(String[] args) { 
      int a = -26;    
      int b = 2;   
      System.out.println(~a); 
      System.out.println(a&b);
      System.out.println(a|b);              
      System.out.println(a^b);
      System.out.println(a>>b);  
      System.out.println(a>>>b); 
      System.out.println(a<<b);   
   } 
}